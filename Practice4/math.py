import math as m
import random

print("Square root:", m.sqrt(25))
print("Power:", m.pow(2, 3))

print("Ceil:", m.ceil(4.2))
print("Floor:", m.floor(4.8))

print("Pi:", m.pi)

print("Sin(90):", m.sin(m.radians(90)))

print("Random number:", random.randint(1, 100))

items = ["Apple", "Banana", "Orange"]

print("Random choice:", random.choice(items))